// event listener - nav a click
function initListeners(){
    $("header nav a").click(function(e){
            
        // this stores the id from the anchor tag
        let aID = e.currentTarget.id;
        let contentID = aID + "Content";

        // changes content of section #app depending on anchor clicked
        $("#app").html(eval(contentID));
        
        // logs aID to console
        // console.log("a id " + aID);
    });
}

// calls functions
$(document).ready(function () {
    initListeners();
    $("#app").html(homeContent); //defaults to homeContent on page load
});